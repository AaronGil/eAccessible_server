package pojo;

public class InformacioAccessibilitatLocal {
	private Integer codiLocal;
    private Integer codiCaracteristica;
    private String nomCaracteristicaCA;
    private Integer valor;
    
    
    public Integer getCodiLocal() {
        return codiLocal;
    }
    public void setCodiLocal(Integer codiLocal) {
        this.codiLocal = codiLocal;
    }
    public Integer getCodiCaracteristica() {
        return codiCaracteristica;
    }
    public void setCodiCaracteristica(Integer codiCaracteristica) {
        this.codiCaracteristica = codiCaracteristica;
    }
    public String getNomCaracteristicaCA() {
        return nomCaracteristicaCA;
    }
    public void setNomCaracteristicaCA(String nomCaracteristicaCA) {
        this.nomCaracteristicaCA = nomCaracteristicaCA;
    }
    public Integer getValor() {
        return valor;
    }
    public void setValor(Integer valor) {
        this.valor = valor;
    }
}
